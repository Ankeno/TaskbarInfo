        public void Timer()
        {
            time = new System.Timers.Timer();
            time.Interval = refreshInterval;
            time.Elapsed += new ElapsedEventHandler(TimeRefresh);
            time.AutoReset = true;
            time.Enabled = true;
        }

        public void TimeRefresh(Object source, System.Timers.ElapsedEventArgs e)
        {
            UpdateProgress();
        }

        public void UpdateProgress()
        {
            float currentSongTime = mbApiInterface.Player_GetPosition();
            float totalSongTime = mbApiInterface.NowPlaying_GetDuration();
            songProgress = currentSongTime / totalSongTime * 100;

            TaskbarProgress.SetValue(mbApiInterface.MB_GetWindowHandle(), songProgress, 100);

            switch (mbApiInterface.Player_GetPlayState())
            {
                case PlayState.Playing:
                    TaskbarProgress.SetState(mbApiInterface.MB_GetWindowHandle(), TaskbarProgress.TaskbarStates.Normal);
                    break;
                case PlayState.Paused:
                    TaskbarProgress.SetState(mbApiInterface.MB_GetWindowHandle(), TaskbarProgress.TaskbarStates.Paused);
                    break;
            }

        }
